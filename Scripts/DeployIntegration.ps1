<#
.Synopsis
   Deploys the AX to Management Reporter integration
.DESCRIPTION
   Deploys the AX to Management Reporter integration using the values contained within $MRDefaultValues or $config.
#>
[CmdletBinding()]
Param
(
	# Log file set from Deployment task.
	[string]
	$log,

	# JSON encoded dictionary containing configuration settings from Deployment task.
	[string]
	$config,

	# Used by DSC, actual XML file in base64 form. Use if cert data/info is needed.
	[string]
	$serviceModelXml,

	 # Values used to override MR's default parameters.
	[Hashtable]
	$MRDefaultValues,

	# Indicates to enforce SQL encryption and SSL certificate is valid.
	[switch]
	$EnforceEncryption,

	# Force Application Service URI and AOS Service URI to use LocalHost regardless of what is passed in
	[switch]
	$ForceLocalHost,

	# Data that was deployed. 'None' if no DDM data is to be deployed.
	[string]
	$DDMData = 'None'
)

. "$PSScriptRoot\ScriptSetup.ps1"
Set-LogPath -FullLogPath $log
Write-EnvironmentDataToLog
$Settings = Get-SettingsObject -Config $config -ServiceModelXml $serviceModelXml
. "$PSScriptRoot\AdditionalFunctions.ps1"

$deployIntegrationScript = New-ScriptExecution -Name 'Deploying integration' `
	-ErrorCode $ErrorCodes.DeployIntegration `
	-Script {
		Import-MRDeployModule
		if(!$MRDefaultValues)
		{
			$MRDefaultValues = @{}
		}

		if($Settings)
		{
			Update-EnforceEncryption -Settings $Settings -EnforceEncryptionReference:([ref]$EnforceEncryption)
			Update-ForceLocalHost -Settings $Settings -ForceLocalHostReference:([ref]$ForceLocalHost)
			Update-DataMartData -Settings $Settings -DDMDataReference:([ref]$DDMData)
			Update-DataMartDataAccess -Settings $Settings -MRDefaultValuesReference:([ref]$MRDefaultValues)
			Update-AXDataAccess -Settings $Settings -MRDefaultValuesReference:([ref]$MRDefaultValues)
			Update-AXAuthenticationSettings -Settings $Settings -MRDefaultValuesReference:([ref]$MRDefaultValues)
			Update-AXSslCertThumbprint -Settings $Settings -MRDefaultValuesReference:([ref]$MRDefaultValues)
			Update-ServerName -Settings $Settings -MRDefaultValuesReference:([ref]$MRDefaultValues)
		}

		if($EnforceEncryption)
		{
			Write-LogMessage -Message 'Enforcing encryption'
			$MRDefaultValues.AXSqlEncryptConnection = $true
			$MRDefaultValues.AXSqlTrustServerCertConnection = 'False'
		}

		Set-ApplicationServerName -ForceLocalHost:$ForceLocalHost -MRDefaultValuesReference:([ref]$MRDefaultValues)
		Set-AXServerName -ForceLocalHost:$ForceLocalHost -MRDefaultValuesReference:([ref]$MRDefaultValues)
		. "$PSScriptRoot\UpdateDefaultValues.ps1"
		Remove-AllAXFinancialReportRecords
		Write-LogMessage 'Getting data mart connection string'
		$sqlConnectionStringParams = Get-SqlConnectionStringParams
		$dataMartConnectionString = Get-SqlConnectionString @sqlConnectionStringParams
		Write-LogMessage 'Got data mart connection string'
		if(!$dataMartConnectionString)
		{
			throw 'Data mart connection string could not be retrieved.'
		}

		Publish-DataMartDatabaseSettings
		$addedWebBinding = Add-LocalHostWebBinding
		try
		{
			Publish-ChangeTracking
			Publish-AXDatabaseSettings
			Write-SchemaUserPrivileges
			Publish-AXIntegrationGroup -DataMartConnectionString $dataMartConnectionString
		}
		finally
		{
			if ($addedWebBinding)
			{
				if($ForceLocalHost)
				{
					Write-LogMessage -Message 'Localhost web binding will permanently exist on this environment ForceLocalHost was set to true' -Warning
				}
				else
				{
					Remove-LocalHostWebBinding
				}
			}
		}

		Update-IntegrationTriggers
	}

Invoke-ExecutionScript -ExecutionScript $deployIntegrationScript
