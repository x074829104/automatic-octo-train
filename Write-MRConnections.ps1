[CmdletBinding()]
Param(
[Parameter(Mandatory=$true)]
[string][ValidateNotNullOrEmpty()] $MRDatabaseName,
[Parameter(Mandatory=$true)]
[string][ValidateNotNullOrEmpty()] $MRServer,
[Parameter(Mandatory=$true)]
[string][ValidateNotNullOrEmpty()] $MRRuntimeUser,
[Parameter(Mandatory=$true)]
[string][ValidateNotNullOrEmpty()] $MRRuntimeUserPassword
)

if (!$env:SERVICEDRIVE)
{
    Write-Warning '%SERVICEDRIVE% is not defined, setting to $env:SystemDrive'
    $env:SERVICEDRIVE = $env:SystemDrive
}

$serviceDrive = $env:SERVICEDRIVE # hard-code this value
$isOneBox = $false

$mrInstallLocation = "C:\FinancialReporting"
$mrProcessInstallLocation = Join-Path -Path $serviceDrive -ChildPath "MRApplicationService\MRInstallDirectory"
$mrApplicationInstallLocation = Join-Path -Path $serviceDrive -ChildPath "MRProcessService\MRInstallDirectory"
$mrOneBoxInstallLocation = Join-Path -Path $serviceDrive -ChildPath "MROneBox\MRInstallDirectory"
$mrDevInstallLocation = Join-Path -Path $serviceDrive -ChildPath "FinancialReporting"

if ((Test-Path -Path $mrProcessInstallLocation))
{
    $mrInstallLocation = $mrProcessInstallLocation
}

if ((Test-Path -Path $mrApplicationInstallLocation))
{
    $mrInstallLocation = $mrApplicationInstallLocation
}

if ((Test-Path -Path $mrOneBoxInstallLocation))
{
    $mrInstallLocation = $mrOneBoxInstallLocation
    $isOneBox = $true
}

if ((Test-Path -Path $mrDevInstallLocation))
{
    $mrInstallLocation = $mrDevInstallLocation
    $isOneBox = $true
}

if (!(Test-Path -Path $mrInstallLocation))
{
    throw "Couldn't find the MRInstallDirectory in any of the expected locations."
}

Import-Module $mrInstallLocation\Server\MRDeploy\MRDeploy.psm1
if($isOneBox)
{
    Write-ServiceSqlConnectionString -Path "$mrInstallLocation\Server\ApplicationService\bin\MRServiceHost.exe.config" -ConnectionSourceFile "MRServiceHost.connections.config" -DatabaseName "$MRDatabaseName" -DatabaseServerName "$MRServer" -DatabaseUserName "$MRRuntimeUser" -DatabaseUserPassword (ConvertTo-SecureString "$MRRuntimeUserPassword" -AsPlainText -Force)
    Write-ServiceSqlConnectionString -Path "$mrInstallLocation\Server\Services\MRServiceHost.exe.config" -ConnectionSourceFile "MRServiceHost.connections.config" -DatabaseName "$MRDatabaseName" -DatabaseServerName "$MRServer" -DatabaseUserName "$MRRuntimeUser" -DatabaseUserPassword (ConvertTo-SecureString "$MRRuntimeUserPassword" -AsPlainText -Force)
}
else
{
    if(Test-Path -Path "$mrInstallLocation\Server\ApplicationService\bin")
    {
        Write-ServiceSqlConnectionString -Path "$mrInstallLocation\Server\ApplicationService\bin\MRServiceHost.exe.config" -ConnectionSourceFile "MRServiceHost.connections.config" -DatabaseName "$MRDatabaseName" -DatabaseServerName "$MRServer" -DatabaseUserName "$MRRuntimeUser" -DatabaseUserPassword (ConvertTo-SecureString "$MRRuntimeUserPassword" -AsPlainText -Force) -UseSsl -TrustServerCertificate "False"
    }
    
    Write-ServiceSqlConnectionString -Path "$mrInstallLocation\Server\Services\MRServiceHost.exe.config" -ConnectionSourceFile "MRServiceHost.connections.config" -DatabaseName "$MRDatabaseName" -DatabaseServerName "$MRServer" -DatabaseUserName "$MRRuntimeUser" -DatabaseUserPassword (ConvertTo-SecureString "$MRRuntimeUserPassword" -AsPlainText -Force) -UseSsl -TrustServerCertificate "False"
}
